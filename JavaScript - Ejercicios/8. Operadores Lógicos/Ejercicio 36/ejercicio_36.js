const numero1 = 10;
const numero2 = 20;
const esIgual = numero1 == numero2;
const esDiferente = numero1 != numero2;

console.log(`La variable numero1: ${numero1} y numero2: ${numero2} tienen el mismo valor: ${esIgual}`);

console.log(`Las variables numero1: ${numero1} y numero2: ${numero2} son distintas: ${esDiferente}`);